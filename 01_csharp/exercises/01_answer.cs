// 対応章：01章「型・変数・制御構文」
// 演習内容：変数の宣言と初期化、制御構文（if・for・foreach）、型変換の解答例

using System;

class Answer01
{
    static void Main()
    {
        // ============================================================
        // 問題1：変数の宣言と型
        // ============================================================

        int    score      = 100;    // 整数型：HP・スコア・カウントなど
        float  speed      = 3.5f;   // float リテラルには f を付ける（付けないと double になる）
        bool   isAlive    = true;   // true / false のみ代入可能
        string playerName = "勇者"; // 文字列はダブルクォートで囲む

        Console.WriteLine($"スコア：{score}、速度：{speed}、生存：{isAlive}、名前：{playerName}");


        // ============================================================
        // 問題2：制御構文
        // ============================================================

        int score2 = 75;

        // 条件式を比較演算子（>=）で記述する
        // else if を増やすことで複数の条件分岐も書ける
        if (score2 >= 80)
        {
            Console.WriteLine("合格");
        }
        else
        {
            Console.WriteLine("不合格");
        }


        // ============================================================
        // 問題3：繰り返し
        // ============================================================

        // i = 1 から始めて 10 以下の間ループ、1 ずつ増やす
        for (int i = 1; i <= 10; i++)
        {
            Console.WriteLine(i);
        }


        // ============================================================
        // 問題4：foreach
        // ============================================================

        string[] weapons = { "剣", "弓", "魔法" };

        // foreach は配列・List などコレクション全般に使える
        // インデックスが不要なときは for より foreach が読みやすい
        foreach (string weapon in weapons)
        {
            Console.WriteLine(weapon);
        }


        // ============================================================
        // 問題5：型変換
        // ============================================================

        string numberText = "42";

        // TryParse は変換に失敗しても例外を投げないため安全
        // 変換できた場合は success = true、result に変換後の値が入る
        if (int.TryParse(numberText, out int result))
        {
            Console.WriteLine($"変換結果：{result}");
        }
        else
        {
            Console.WriteLine("変換に失敗しました");
        }

        // int.Parse でも変換できるが、失敗した場合に例外が発生する
        // int result2 = int.Parse(numberText); // "abc" などを渡すとエラー
    }
}
