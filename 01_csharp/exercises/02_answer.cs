using System;

// ============================================================
// 問題1・3・4：Player クラスの定義
// ============================================================

class Player
{
    // private フィールド：クラスの外から直接変更できないようにする
    private string name;
    private int    hp;

    // コンストラクタ：this.name でフィールドとパラメータを区別する
    public Player(string name, int hp)
    {
        this.name = name;
        this.hp   = hp;
    }

    // メソッド：ダメージを受ける処理
    public void TakeDamage(int damage)
    {
        hp -= damage;
        if (hp < 0) hp = 0; // 0 未満にならないようガード
    }

    // メソッド：状態を出力する
    public void PrintStatus()
    {
        Console.WriteLine($"{name}：HP {hp}");
    }

    // 問題3：プロパティ（get のみ）
    // フィールドを直接公開せずプロパティ経由で読み取れるようにする
    // set を省くことで外部からの書き込みを防げる
    public int Hp
    {
        get { return hp; }
    }

    // 問題4：static メソッド
    // static を付けるとインスタンスを作らずにクラス名で呼び出せる
    public static void PrintGameTitle()
    {
        Console.WriteLine("スペースシューター");
    }
}

// ============================================================
// メインの実行コード
// ============================================================

class Answer02
{
    static void Main()
    {
        // ============================================================
        // 問題2：インスタンスの生成
        // ============================================================

        // new キーワードでインスタンスを生成する
        // コンストラクタの引数の順番に注意（name, hp の順）
        Player player = new Player("勇者", 100);

        player.TakeDamage(30);   // HP を 30 減らす
        player.PrintStatus();    // 勇者：HP 70


        // ============================================================
        // 問題3：プロパティ
        // ============================================================

        // プロパティ経由で HP を読み取る
        // フィールド hp は private なので直接アクセスできない
        int currentHp = player.Hp;
        Console.WriteLine($"現在の HP：{currentHp}"); // 現在の HP：70


        // ============================================================
        // 問題4：static メソッド
        // ============================================================

        // インスタンスを作らず、クラス名で直接呼び出す
        Player.PrintGameTitle(); // スペースシューター
    }
}
